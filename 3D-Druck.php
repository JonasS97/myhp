    <h4>Was ist 3D Druck und wie funktioniert es</h4><br>
    Bei eim 3D-Druck mit Filament wird ein Kunststoffdraht (Filament) in einer beheizten Düse geschmolzen und schichtweise auf einer Plattform abgelagert. Der Drucker bewegt die Düse gemäß dem digitalen 3D-Modell, wobei jede Schicht des Objekts nacheinander aufgetragen wird, bis das gesamte Modell aufgebaut ist. Das Verfahren heißt FDM (Fused Deposition Modeling) und ist besonders beliebt, da es kostengünstig und für zahlreiche Materialien geeignet ist.
<br><br>
<h4>Die Gefahren beim 3D Drucken</h4>
Beim 3D-Druck mit Filament entstehen gesundheitliche Gefahren hauptsächlich durch feine Partikel (Ultrafeinstaub) und flüchtige organische Verbindungen (VOCs), die beim Erhitzen des Filaments freigesetzt werden. Diese Stoffe können eingeatmet werden und die Atemwege reizen, besonders bei Filamenten wie ABS oder Nylon, die mehr VOCs abgeben. Langfristige Exposition könnte Atemwegserkrankungen oder allergische Reaktionen fördern.
    <br><br>
    <h4>Welche Kosten enstehen beim 3D Drucken</h4>
    Die Kosten beim 3D-Drucken mit Filament setzen sich aus mehreren Faktoren zusammen:

1. Drucker:
Einsteiger-3D-Drucker: Zwischen 150 € und 500 € für einfachere Modelle.
Professionelle 3D-Drucker: Kosten oft zwischen 1.000 € und 10.000 € oder mehr, je nach Qualität und Funktionsumfang.
2. Filament:
Die Kosten für 3D-Druck-Filament variieren je nach Material:
PLA (ein gängiges, umweltfreundliches Material): Ca. 20 € bis 40 € pro Kilogramm.
ABS: Etwa 25 € bis 45 € pro Kilogramm.
Spezialmaterialien (wie PETG, TPU, Nylon, etc.): Preise können zwischen 30 € und 80 € pro Kilogramm liegen.
3. Stromverbrauch:
Der Stromverbrauch hängt vom Drucker und der Druckdauer ab, aber grob kann man mit 1 bis 2 € pro Druck rechnen, je nach Druckdauer und -größe.
4. Wartung und Ersatzteile:
Düsen, Heizbetten, Federn und andere Verschleißteile können gelegentlich ersetzt werden, was zusätzliche 10 € bis 50 € pro Jahr kosten kann.
5. Software:
Slicer-Software wie Cura ist oft kostenlos. Professionelle Software oder spezialisierte Programme können jedoch kostenpflichtig sein (zwischen 100 € und 500 € je nach Software).
6. Zusätzliche Kosten (optional):
Werkzeug: Zangen, Spachtel, Reinigungspinsel usw. (ca. 10 € bis 50 €).
Zubehör wie Druckbett-Heizung, Lüfter oder Filamenttrockner: Wenn benötigt, können zusätzliche Kosten von 20 € bis 100 € entstehen.
Insgesamt sind die laufenden Kosten beim 3D-Drucken relativ niedrig, es hängt jedoch stark davon ab, wie häufig und auf welche Weise der Drucker genutzt wird.
